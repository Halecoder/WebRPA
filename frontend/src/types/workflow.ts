// 工作流节点
export interface WorkflowNode {
  id: string
  type: ModuleType
  position: Position
  data: ModuleConfig
}

// 工作流边
export interface WorkflowEdge {
  id: string
  source: string
  target: string
  sourceHandle?: string | null
  targetHandle?: string | null
}

// 变量类型
export type VariableType = 'string' | 'number' | 'boolean' | 'array' | 'object'

// 变量
export interface Variable {
  name: string
  value: unknown
  type: VariableType
  scope: 'global' | 'local'
  description?: string  // 变量描述（内置变量使用）
  builtin?: boolean     // 是否为内置隐含变量
}

// 工作流
export interface Workflow {
  id: string
  name: string
  nodes: WorkflowNode[]
  edges: WorkflowEdge[]
  variables: Variable[]
  createdAt: string
  updatedAt: string
}

// 模块配置基类
export interface ModuleConfig {
  name?: string
  description?: string
  timeout?: number
  timeoutAction?: 'retry' | 'skip'  // 超时后的处理方式：重试或跳过
  retryCount?: number
  [key: string]: unknown
}

// 执行状态
export type ExecutionStatus = 'pending' | 'running' | 'completed' | 'failed' | 'stopped'

// 执行结果
export interface ExecutionResult {
  workflowId: string
  status: ExecutionStatus
  startedAt: string
  completedAt?: string
  totalNodes: number
  executedNodes: number
  failedNodes: number
  errorMessage?: string
  dataFile?: string
}

// 日志级别
export type LogLevel = 'debug' | 'info' | 'warning' | 'error' | 'success'

// 日志条目
export interface LogEntry {
  id: string
  timestamp: string
  level: LogLevel
  nodeId?: string
  message: string
  details?: Record<string, unknown>
  duration?: number
}
